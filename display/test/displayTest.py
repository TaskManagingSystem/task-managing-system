def RunTest():
    manager = OpenRTM_aist.Manager.instance()
    comp = manager.getComponent("displayTest0")
    if comp is None:
        print('Component get failed.', file=sys.stderr)
        return False
    return comp.runTest()

def displayTestInit(manager):
    profile = OpenRTM_aist.Properties(defaults_str=displaytest_spec)
    manager.registerFactory(profile,
                            displayTest,
                            OpenRTM_aist.Delete)

def MyModuleInit(manager):
    displayTestInit(manager)
    display.displayInit(manager)

    # Create a component
    comp = manager.createComponent("displayTest")

def main():
    mgr = OpenRTM_aist.Manager.init(sys.argv)
    mgr.setModuleInitProc(MyModuleInit)
    mgr.activateManager()
    mgr.runManager(True)

    ret = RunTest()
    mgr.shutdown()

    if ret:
        sys.exit(0)
    else:
        sys.exit(1)

if __name__ == "__main__":
    main()

